    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Conf.kmeans_classes = Integer.parseInt(classes.getSelectedItem().toString());
        if(checkCor.isSelected())
            Conf.feature_color = true;
        else
            Conf.feature_color = false;
        if(checkCurtose.isSelected())
            Conf.feature_curtose = true;
        else
            Conf.feature_curtose = false;
        if(checkMedia.isSelected())
            Conf.feature_media = true;
        else
            Conf.feature_media = false;
        if(checkSkewness.isSelected())
            Conf.feature_skewness=true;
        else
            Conf.feature_skewness=false;
        if(checkVariancia.isSelected())
            Conf.feature_variancia = true;
        else
            Conf.feature_variancia = false;
        
        try {
            BufferedImage imagem = mainFrame2.getSelected().getImage();
            JImageWindow imgWindow = new JImageWindow(imagem, JImageWindow.Tipo.KMEANSGRAY);
            imgWindow.addInternalFrameListener(new InternalFrameAdapter() {
                    @Override
                    public void internalFrameActivated(javax.swing.event.InternalFrameEvent e) {
                            mainFrame2.setSelected((JImageWindow) e.getSource());							
                    };

                    @Override
                    public void internalFrameClosed(javax.swing.event.InternalFrameEvent e) {
                            JImageWindow closed = (JImageWindow) e.getSource();
                            mainFrame2.getDesktopPane().remove(closed);															
                    };
            });

            imgWindow.setVisible(true);
            imgWindow.setLocation(
                            mainFrame2.getSelected().getLocation().x + 50, 
                            mainFrame2.getSelected().getLocation().y + 50);

            mainFrame2.getDesktopPane().add(imgWindow);
            imgWindow.setSelected(true);

            JImageWindow[] clsWindow = new JImageWindow[Conf.kmeans_classes];
            for (int i = 0; i < Conf.kmeans_classes; i++) {
                    clsWindow[i] = new JImageWindow(imagem, JImageWindow.Tipo.CLASSE, i);
                    clsWindow[i].addInternalFrameListener(new InternalFrameAdapter() {
                            public void internalFrameActivated(javax.swing.event.InternalFrameEvent e) {
                                    mainFrame2.setSelected((JImageWindow) e.getSource());
                            };

                            public void internalFrameClosed(javax.swing.event.InternalFrameEvent e) {
                                    JImageWindow closed = (JImageWindow) e.getSource();
                                    mainFrame2.getDesktopPane().remove(closed);									
                            };
                    });

                    clsWindow[i].setVisible(true);
                    clsWindow[i].setLocation(
                                    mainFrame2.getSelected().getLocation().x + 50, 
                                    mainFrame2.getSelected().getLocation().y + 50);

                    mainFrame2.getDesktopPane().add(clsWindow[i]);
                    clsWindow[i].setSelected(true);
                }
            } catch (Exception e1) {
                    JOptionPane.showMessageDialog(mainFrame2, "Falha ao carregar a imagem");
                    e1.printStackTrace();
            }
        //this.setVisible(false);
        //this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

